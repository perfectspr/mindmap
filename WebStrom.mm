<node CREATED="1453267627056" ID="ID_885246631" MODIFIED="1453267699785" TEXT="Zoom">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      To enable changing font size in the editor
    </p>
    <p>
      1.Open the Settings/Preferences dialog, expand the Editor node, and click General.
    </p>
    <p>
      2.Make sure that the setting Change font size (Zoom) with Ctrl+MouseWheel is enabled.
    </p>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      To change font size using the mouse wheel
    </p>
    <p>
      1.Place the caret in the editor.
    </p>
    <p>
      2.While keeping the Ctrl key pressed, rotate the mouse wheel. As you rotate the mouse wheel forward, font size grows larger; as you rotate the mouse wheel backwards, font size decreases.&#160;
    </p>
    <p>
      The OS X users can use trackpad &quot;Pinch-to-Zoom&quot; gesture to change size of the font and the whole editing area.
    </p>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      To reset font size
    </p>
    <p>
      1.Press Ctrl+Shift+A.
    </p>
    <p>
      2.In the popup frame, type Reset font size, and click Enter.
    </p>
    <p>
      The default font size is restored.
    </p>
  </body>
</html>
</richcontent>
